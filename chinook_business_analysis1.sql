-- =========================================================
-- 1. CUSTOMER ANALYSIS
-- =========================================================

-- Retrieve basic customer information
-- Business Insight: Understand customer distribution base
SELECT 
    FirstName,
    LastName,
    Country
FROM customer;


-- Filter customers located in Brazil
-- Business Insight: Identify customers from a specific market segment
SELECT 
    FirstName,
    LastName,
    Country
FROM customer
WHERE Country = 'Brazil';


-- Sort customers alphabetically by last name
-- Business Insight: Improve customer record readability and organization
SELECT 
    FirstName,
    LastName
FROM customer
ORDER BY LastName ASC;


-- Count total number of customers
-- Business Insight: Measure customer base size
SELECT 
    COUNT(*) AS TotalCustomers
FROM customer;


-- =========================================================
-- 2. REVENUE ANALYSIS
-- =========================================================

-- Calculate total revenue generated
-- Business Insight: Overall business performance indicator
SELECT 
    SUM(Total) AS TotalRevenue
FROM invoice;


-- Calculate average invoice value
-- Business Insight: Understand customer spending behavior
SELECT 
    AVG(Total) AS AverageInvoiceValue
FROM invoice;


-- =========================================================
-- 3. REGIONAL PERFORMANCE ANALYSIS
-- =========================================================

-- Revenue generated by each country
-- Business Insight: Identify top-performing markets
SELECT 
    BillingCountry,
    SUM(Total) AS Revenue
FROM invoice
GROUP BY BillingCountry
ORDER BY Revenue DESC;


-- Countries with revenue greater than 100
-- Business Insight: Highlight high-value markets for strategic focus
SELECT 
    BillingCountry,
    SUM(Total) AS Revenue
FROM invoice
GROUP BY BillingCountry
HAVING SUM(Total) > 100
ORDER BY Revenue DESC;




-- =========================================================
